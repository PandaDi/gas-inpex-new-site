export async function generateMetadata({ params }: Props): Promise<Metadata> {
  const { slug } = await params;
  const service = serviceDetails[slug];
  if (!service) return { title: "Услуга не найдена" };
  return {
    title: service.title,
    description: service.description,
  };
}

